function generateHotspots(crimeTally) {
  const crimeRates = Object.values(crimeTally);
  const mean = crimeRates.reduce((a, b) => a + b, 0) / crimeRates.length;
  // Calculate standard deviation
  const variance =
    crimeRates.reduce((a, b) => a + Math.pow(b - mean, 2), 0) /
    crimeRates.length;
  const standardDeviation = Math.sqrt(variance);
  const threshold = mean + 0.5*standardDeviation;

  const hotspots = Object.keys(crimeTally).filter(
    (area) => crimeTally[area] > threshold
  );
  return hotspots;
}

